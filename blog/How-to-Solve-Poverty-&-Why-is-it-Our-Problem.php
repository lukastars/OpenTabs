              <h1 class="mb-4">How to Solve Poverty &amp; Why is it “Our” Problem?</h1>
              <div class="post-meta">

                <span class="mr-2">May 10, 2020 </span>

              </div>
              <div class="post-content-body">
                <p>In 2015, the United Nations set up the 17 global Sustainable Development Goals (SDG), with
                  the first goal to “end poverty in all its forms everywhere”. About 600 million people (8% of
                  the world population) live in extreme poverty, and many people often think of poverty as
                  “their” problem. But the reality is far from it. For example, global pandemics (such as Ebola
                  or COVID-19) are more likely to spur and spread due to a lack of basic health services in
                  parts of the world, and young men who are desperate for food are also more likely to join
                  guerrillas and terrorist groups. Extreme poverty poses a threat to everyone, and we should
                  eradicate it as soon as possible.
                </p>
                <p>Eradicating poverty is not just our socially ethical duty to help those in need, but is also an
                  enabler for achieving the 16 other SDGs. The reason being is simple, it is much harder to
                  think about gender equality, nature conservation, and peace, when you are suffering and
                  struggling to survive on a daily basis. As Ban Ki-moon, former General-Secretary of the UN
                  said, “saving our planet, lifting people out of poverty, advancing economic growth… these
                  are one and the same fight.</p>
                <p>It is a common myth that poverty cannot solved, but we have already drastically reduced it
                  from the 1980s. This means that unlike the threat of a global pandemic, which is riddled with
                  uncertainty, we already know the tools to reduce poverty: peace, education, universal basic
                  health care, access to electricity, clean water, toilets, contraceptives, and micro-loans. The
                  issue now lays in allocating the necessary resources to tackle such significant goal.</p>

                <div class="row mb-5">
                  <div class="col-md-12 mb-4 element-animate">
                    <img src="blog images/Poverty.png" alt="Image placeholder" class="img-fluid">
                  </div>
                </div>
                <p>According to Jeffrey Sachs, one of the world’s leading experts on economic development and
                  the fight against poverty, we could end poverty if every living person donated an estimated
                  $23 a year for 20 years. Of course, this is impossible, so we need the cooperation of
                  individuals, organisations, and governments. <a href="../index.html" target="_blank">OpenTabs</a>was
                  born
                  out of the idea of allowing everyone to passively contribute to this cause without spending
                  any resources. For this reason, we created a browser homepage, a search engine, and a flight
                  finder that allow you to passively raise funds towards providing microloans and preventing
                  malaria. Click <a href="../index.html" target="_blank">here</a>to explore our free products and begin
                  to
                  passively have a positive impact.
                </p>
                <p>The late Hans Rosling was an acclaimed Swedish physician, academic, statistician, and
                  public speaker, who co-founded the Gapminder Foundation. He argued that extreme poverty
                  was one of the five global risks we should be worrying about (along with Global Pandemic,
                  Climate Change, World War III, and Financial Collapse). If you haven’t read it yet, I
                  definitely recommend reading “Factfulness: Ten Reasons We&#39;re Wrong About the World -
                  and Why Things Are Better Than You Think” by Anna Rosling Rönnlund, Hans Rosling, and
                  Ola Rosling. If you need more convincing on why we should tackle poverty, read the
                  following extract from Hans’ book:</p>
                <p><em>The other risks I have mentioned are highly probable scenarios that would bring unknown
                    levels of future suffering. Extreme poverty isn’t really a risk. The suffering it causes is not
                    unknown, and not in the future. It’s a reality. It’s misery, day to day, right now. It is also
                    where Ebola outbreaks come from, because there are no health services to encounter them at
                    an early stage; and where civil wars start, because young men desperate for food and work,
                    and with nothing to lose, tend to be more willing to join brutal guerrilla movements. It’s a
                    vicious circle: poverty leads to civil war, and civil war leads to poverty. The civil conflicts in
                    Afghanistan and central Africa mean that all other sustainability projects in those places are
                    on hold. Terrorists hide in the few remaining areas of extreme poverty. When rhinos are
                    stuck in the middle of a civil war, it’s much more difficult to save them.</em></p>
                <div class="row mb-5">
                  <div class="col-md-12 mb-4 element-animate">
                    <img src="blog images/hans_inline.jpg" alt="Image placeholder" class="img-fluid">
                  </div>
                </div>
                <p><em>Today, a period of relative world peace has enabled a growing global prosperity. A smaller
                    proportion of people than ever before is stuck in extreme poverty. But there are still 800
                    million people left. Unlike with climate change, we don’t need predictions and scenarios. We
                    know that 800 million are suffering right now. We also know the solutions: peace, schooling,
                    universal basic health care, electricity, clean water, toilets, contraceptives, and microcredits
                    to get market forces started. There’s no innovation needed to end poverty. It’s all about
                    walking the last mile with what’s worked everywhere else. And we know that the quicker we
                    act, the smaller the problem, because as long as people remain in extreme poverty they keep
                    having large families and their numbers keep increasing. Providing these necessities of a
                    decent life, quickly, to the final billion is a clear, fact-based priority.</em></p>
                <p><em>The hardest to help will be those stuck behind violent and chaotic armed gangs in weakly
                    governed states. To escape poverty, they will need a stabilizing military presence of some
                    kind. They will need police officers with guns and government authority to defend innocent
                    citizens against violence and to allow teachers to educate the next generation in peace.</em></p>
                <p><em>Still I’m possibilistic. The next generation is like the last runner in a very long relay race.
                    The race to end extreme poverty has been a marathon, with the starter gun fired in 1800. This

                    next generation has the unique opportunity to complete the job: to pick up the baton, cross
                    the line, and raise its hands in triumph. The project must be completed. And we should have a
                    big party when we are done.</em></p>

