          <h1 class="mb-4">What is Malaria and how you can help reduce its damage in the world</h1>
          <div class="post-meta">

            <span class="mr-2">July 7, 2020 </span>

          </div>
          <div class="post-content-body">

            <p>When talking about Malaria, we are typically talking about the fact that it has killed more humans in
              history.</p>
            <p>It is believed that this illness has a prehistoric origin due to the fact that the transmitter species
              have spent more time on the earth than us. Moreover, some studies have found registers where Greeks talked
              about an epidemic of sporadic fever in the regions where the disease is present.
            </p>
            <p>In 2018 more than 200 million people were infected and around 405 000 have died because of it.
            </p>
            <p>The population more vulnerable to this disease are children between 0 and 5 years old being 67% of total
              deaths. Furthermore, people from Africa suffer 94% of the total deaths.
            </p>
            <div class="row mb-5">
              <div class="col-md-12 mb-4 element-animate">
                <img src="blog images/Malaria deaths per year.png" alt="Image placeholder" class="img-fluid">
              </div>
            </div>
            <h2>How is Malaria transmitted</h2>
            <p><b>Malaria is not a contagious illness</b> between humans like flu, but there are four ways where you can
              catch it if another person around you has it, these ways are the following ones:</p>
            <ol>
              <li>An infected person did a transfusion of blood to you</li>
              <li>An infected person gives an organ to you</li>
              <li>Use of shared needles or syringes with a carrier
              </li>
              <li>Your mother was infected during her pregnancy (this only might happen with the parasite P. falciparum)
              </li>
            </ol>
            <p>Generally,<b> Malaria is caused by</b><a href="https://en.wikipedia.org/wiki/Anopheles" target="_blank">
                Anopheles mosquitoes</a>. These animals bring inside the Plasmodium parasite and when they bite you the
              parasite is found in your circulatory system.
            </p>
            <div class="row mb-5">
              <div class="col-md-12 mb-4 element-animate">
                <img src="blog images/Anopheles mosquitoes.png" alt="Image placeholder" class="img-fluid">
              </div>
            </div>
            <p>When the parasite is in your body, first it travels by your liver where it spends nearly a month hiding
              from your immune system and avoids a response from it. During this time, they are patiently waiting to
              mature, after they burst out to the bloodstream and start to infect red blood cells. </p>
            <p>Even when thousands of this parasite have killed our red blood cells, they are still unable to detect our
              defenses due they wrap themselves in the membranes of the cell that have murdered.
            </p>
            <p>They repeat over and over this cycle, the pieces of dead cells spread toxic material which activates the
              immune system causing different kinds of symptoms, even arriving to produce neurological damage.</p>
            <div class="row mb-5">
              <div class="col-md-12 mb-4 element-animate">
                <img src="blog images/malaria_LifeCycle.jpg" alt="Image placeholder" class="img-fluid">
              </div>
            </div>
            <div class="row mb-5">
              <div class="col-lg-12 col-md-12 col-sm-12 mb-4 element-animate">
                <div class="embed-responsive embed-responsive-16by9">
                  <iframe class="embed-responsive-item" src="https://www.youtube.com/embed/TnzcwTyr6cE?start=152"
                    allowfullscreen></iframe>
                </div>
              </div>
            </div>
            <h2>Malaria Symptoms</h2>
            <p>Normally, the symptoms start to appear within 10 days to 4 weeks, although in some strange cases these
              symptoms do not make presence until several months.</p>
            <p>Principal symptoms are the following ones:</p>
            <ol>
              <li>Shaking chills that can range from moderate to severe
              </li>
              <li>High fever
              </li>
              <li>Abundant sweating
              </li>
              <li>Headaches
              </li>
              <li>Nauseous
              </li>
              <li>Abdominal pain
              </li>
              <li>Diarrhea</li>
              <li>Anemia</li>
              <li>Muscle pain</li>
              <li>Convulsions</li>
              <li>Coma</li>
              <li>Bloody stools</li>
            </ol>
            <p>Besides we have to consider the less common problems that may cause this disease. The following
              complications can be caused by Malaria:
            </p>
            <ol>
              <li>Swelling of the blood vessels of the brain, or cerebral malaria</li>
              <li>An accumulation of fluid in the lungs that causes breathing problems, or pulmonary edema</li>
              <li>Organ failure of the kidneys, liver, or spleen
              </li>
              <li>Low blood sugar
              </li>
            </ol>
            <h2>TYPES OF MALARIA</h2>
            <p>There are different kinds of <b> malaria parasites</b>, all of them are transmitted in the same way (the
              ones we mentioned before) but they are not equal, the effects on each one of those can be significantly
              important.
            </p>
            <h3>Plasmodium vivax</h3>
            <p>This pathogen is the most frequent between people infected by malaria. Their effects can develop severe
              disease and in the worst cases death, although is not the parasite with the highest rate of mortality.</p>
            <p><b>Plasmodium vivax</b> is found principally in Asia, Latin America, and some parts of Africa.
            </p>
            <p><b> P. Vivax</b> can lead to unusual symptoms like a hiccup, loss of taste, lack of fever, pain while
              swallowing, cough, and urinary discomfort.
            </p>
            <div class="row mb-5">
              <div class="col-md-12 mb-4 element-animate">
                <img src="blog images/Plasmodium_vivax.png" alt="Image placeholder" class="img-fluid">
              </div>
            </div>
            <h3>Plasmodium Falciprum</h3>
            <p>It is the parasite that causes more deaths between humans, it was responsible for 405,000 demises.
            </p>
            <p><b> P. Falciparum</b> is also associated with the development of blood cancer and is classified as Group
              2A carcinogen.
            </p>
            <p>The area where you can catch this pathogen easier is the Sub-Saharan zone, on this place near 75% of
              cases were located last year.</p>
            <div class="row mb-5">
              <div class="col-md-12 mb-4 element-animate">
                <img src="blog images/Plasmodium_falciparum.png" alt="Image placeholder" class="img-fluid">
              </div>
            </div>
            <h3>Plasmodium Malariae</h3>
            <p>It is the source of the “benign malaria”, this name was given because of the gravity of the illness
              compared with their brothers.</p>
            <p>The symptom of this disease is fever approximately three days intervals, a lit bit longer than the other
              ones (two days interval).
            </p>
            <div class="row mb-5">
              <div class="col-md-12 mb-4 element-animate">
                <img src="blog images/Plasmodium_malariae.jpg" alt="Image placeholder" class="img-fluid">
              </div>
            </div>
            <h3>Plasmodium Ovale</h3>
            <p>It is responsible for tertian malaria, another benign malaria. Is one of the most uncommon pathogens
              found and it has two subspecies P. ovale curtisi and P. ovale wallikeri.
            </p>
            <p>This infection causes fever every 49 hours but the symptoms disappear after the period 12 or 20 days.
            </p>
            <p>Is generally located in sub-Saharan Africa and Island from the western pacific but also isolated cases
              were found in India, Philippines, Thailand, or Vietnam among others.
            </p>
            <div class="row mb-5">
              <div class="col-md-12 mb-4 element-animate">
                <img src="blog images/Plasmodium_ovale.png" alt="Image placeholder" class="img-fluid">
              </div>
            </div>
            <h3>Plasmodium knowlesi</h3>
            <p>Is the most common parasite located in Malaysia, it causes severe and uncomplicated malaria in humans.
            </p>
            <p>Their effects are fever, chills, headaches, and the loss of appetite. The fevers caused by this parasite
              are every 24 hours (more frequently than other types of the parasite).
            </p>
            <p>How is <b> P. knowlesi</b> treated depends on which kind of malaria you have, at least 10% of the cases
              caused by this pathogen are uncomplicated.</p>
            <div class="row mb-5">
              <div class="col-md-12 mb-4 element-animate">
                <img src="blog images/plasmodium knowlesi.jpeg" alt="Image placeholder" class="img-fluid">
              </div>
            </div>
            <h2>How to prevent malaria</h2>
            <p>If you are going to travel, we recommend to you to search <a
                href="https://www.cdc.gov/malaria/travelers/country_table/a.html" target="_blank"> how people prevent
                malaria in that specific country</a>, because the effectiveness of medicine could differ depending on
              what kind of parasite mosquitoes have on that location.
            </p>
            <div class="row mb-5">
              <div class="col-md-12 mb-4 element-animate">
                <img src="blog images/Malaria map.jpg" alt="Image placeholder" class="img-fluid">
              </div>
            </div>
            <p>However, we leave you here the list of the most common <b> prevention medicines for malaria</b>, to be
              sure what you should use visit your doctor, consider that you might have other medications which may cause
              secondary effects or allergies that do not let you take some medicines.
            </p>
            <p>Most common drugs for <b> malaria prophylaxis</b>:
            </p>
            <ul>
              <li><b>Autovaquone/Proguanil (Malarone)
                </b></li>
              <li><b>Chloroquine
                </b></li>
              <li><b>Doxycline</b></li>
              <li><b>Mefloquine
                </b></li>
              <li><b>Primaquine
                </b></li>
              <li><b>Tafenoquine (ArakodaTM)</b></li>
            </ul>
            <p>There is not any <b> antimalarial medicine</b> capable of being 100% protective, for this reason, needs
              to be combined with other measures of prevention as the following ones:
            </p>
            <ul>
              <li>Apply insect repellent to exposed skin. The recommended repellent contains 20-35% percent N,
                N-Diethyl-meta-toluamide (DEET).</li>
              <li>Wear long-sleeved clothing and long pants if you are outdoors at night.
              </li>
              <li>Use a mosquito net over the bed if your bedroom is not air-conditioned or screened. For additional
                protection, treat the mosquito net with the insecticide permethrin.
              </li>
              <li>Spray an insecticide or repellent on clothing, as mosquitoes may bite through thin clothing.
              </li>
              <li>Spray pyrethrin or a similar insecticide in your bedroom before going to bed.
              </li>
            </ul>
            <h2>MALARIA TREATMENT</h2>
            <p><b> There is not a vaccine for malaria</b>, for this reason, the illness has been treated with drugs. It
              is the only way proven by scientific people who has an effect.
            </p>
            <p>There are many variables that you might consider when you are going to take some medicine or another, for
              this reason, we highly recommend you visit your doctor at the hospital, they will prescribe you the best
              treatment to fight against malaria.
            </p>
            <p>The variables that a specialist might consider are the following ones:
            </p>
            <ul>
              <li>Which type of malaria you have
              </li>
              <li>The grade of the symptoms
              </li>
              <li>Your age
              </li>
              <li>Whether you are pregnant
              </li>
            </ul>
            <h3>Medication </h3>
            <p>The disease could be threatened in many ways, on the following list you could see the different
              treatments available:
            </p>
            <ul>
              <li><b>IV artesunate
                </b>(for several Malaria)</li>
              <li><b>Artemether-lumefantrine</b></li>
              <li><b>Chloroquine
                </b>or hydroxychloroquine</li>
              <li><b>Mefloquine
                </b></li>
              <li><b>Atovaquone-proguanil
                </b></li>
              <li><b>Clindamycin</b></li>
              <li><b>Tetracycline
                </b></li>
            </ul>
            <p>These are some of the possible options, this due there are different kinds of pathogens and every person
              has a different health record, for this, we cannot recommend taking one instead of another. </p>
            <h2>How I can help to stop malaria</h2>
            <p>OpenTabs is supporting the <a href="https://againstmalaria.com/" target="_blank"> Against Malaria
                Foundation</a> with regular donations. 100% of the donations we
              make to AMF will be used to buy nets for those in need and who can’t afford them.</p>
            <p>We have ways to raise funds (one of them won’t cost you money).
            </p>
