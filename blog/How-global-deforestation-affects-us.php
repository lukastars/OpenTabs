          <h1 class="mb-4">How global deforestation affects us</h1>
          <div class="post-meta">

            <span class="mr-2">July 5, 2020 </span>

          </div>
          <div class="post-content-body">
            <p>To start we must know deforestation’s definition to know what we are talking about
              since it goes beyond what people think, simply based solely on the <b> Amazon
                rainforest deforestation </b>or the Australian fires since they are the most recent
              <b>deforestation examples</b>. but what is deforestation really?
            </p>

            <h2>What is deforestation?</h2>
            <p>Deforestation is a process caused by human activity focused on <b> rainforest
                destruction</b> by different ways like burning forest, which destroys the entire forest
              area of any type of environment, either for reasons of the wood industry as well as
              obtaining soils for agriculture or for mining or livestock purposes, furthermore, human
              accidents that generate a fire must be considered, thus generating a burning of trees
              and involuntary vegetation.</p>

            <p>It is deforestation that means that the forests and jungles of the land are devoid of all
              kinds of vegetation and that causes a great deal of damage associated with the
              quality of the soil.</p>

            <h2>Causes of deforestation</h2>
            <p>Deforestation can occur due to natural causes or due to human activities. A fire,
              without going any further, that produces deforestation can have its origin in man or
              be caused by nature, such as when lightning strikes a tree.</p>
            <p>Currently, the rate of deforestation is too high. Forests are ecosystems that recover
              very slowly. Some varieties of flora species and, indirectly, the fauna that lives there,
              are in danger if the forest disappears.</p>
            <p>We also have to take into account the time it takes for an ecosystem to regenerate
              since according to the professor of ecosystem science at the University of Oxford
              (England) Yadvinder Malhi assures that &quot;the forest takes between 20 and 40 years if
              it is allowed to regenerate &quot;said in a <a
                href="https://www.bbc.com/mundo/noticias-america-latina-49483620" target="_blank"> BBC</a> article in
              August 2019.</p>

            <h2>Effects of deforestation</h2>
            <p>Forests are complex ecosystems that are important to the carbon and water cycles
              that sustain life on earth. When they are degraded, a series of devastating events
              can be triggered both locally and globally. Among the effects, we can find things like

              loss of species, carbon emissions, alterations of the water cycle, soil erosion among
              other causes</p>
            <p>Habitat loss can lead to species extinction. This is not only a tragedy of biodiversity,
              but also has negative consequences for local populations.</p>

            <p>Healthy forests help absorb greenhouse gases and carbon emissions caused by
              human civilization and contribute to global climate change.</p>

            <p>Without trees, more carbon gases and effect gases enter the atmosphere. Trees
              actually become sources of carbon when cut as they are felled and burned,
              removing large amounts of CO2 into the atmosphere.</p>

            <p>Trees play an important role in the water cycle, as they absorb it through the ground
              through their roots and then release it into the atmosphere. In the Amazon, more
              than half of the water in the ecosystem is kept within plants. Without plants, the
              climate can become arid and uncultivable. For those reason the chapters of the
              <b>Brazilian deforestation is a huge problem for the climate change</b>.</p>

            <p>Without tree roots to anchor the soil and with increased exposure to the sun, the soil
              can dry out, leading to problems such as increased flooding and the inability to farm
              on farms. All of these factors can have adverse effects on local economies.</p>

            <p>The increase in floods, the lack of quality water, and the inability to produce their
              own food causes many villagers to migrate to cities that lack infrastructure for them.
              Indiscriminate deforestation of trees is undoubtedly a big problem for today&#39;s and
              future society.</p>

            <h2>Deforestation and its fauna</h2>

            <p>Deforestation has many negative effects on the environment. The most dramatic
              impact is the loss of habitat for millions of species. Seventy percent of animals and
              plants inhabit Earth&#39;s forests, and many cannot survive the deforestation that
              destroys their environment.</p>

            <p>Among the birds, woodpeckers, parrots, and toucans are the most threatened
              species, but it highlights the serious situation of wild turkeys: they are the size of
              chickens and need large trees, it is a species that needs the fruits of, so it is very
              threatened.</p>

            <p>By contrast, graminivorous birds, those that feed on grain, are on the rise, because
              forests are often replaced by farmland.</p>

            <p>Many animals do not survive the first destruction of their habitats. Many species can
              live without realizing deforestation in their area until it is underway.</p>

            <p>When a tree falls, it can kill many of the animals that live in its trunk and crown.
              Others who survive the initial destruction may not know where to go. Without the
              food and shelter provided by the tree, they sometimes die when exposed.</p>

            <h2>Why is deforestation bad</h2>

            <p>Stopping deforestation is important to curb climate change in the world since forests
              are a reserve of carbon and give oxygen and water.</p>

            <p> Forests play a vital role in the global carbon cycle as one of the most important
              carbon sinks. Due to its role in climate balance, reforestation should be prioritized to
              reduce the effects of the crisis due to climate change.</p>

            <p> Working to end deforestation and forest degradation while helping to restore lost
              forests is our best chance to resolve the climate emergency, protect wildlife, and
              defend the rights of indigenous people and traditional local communities.</p>

            <p>Historically, the <a href="https://www.greenpeace.org/usa/campaigns/forests/" target="blank">
                Greenpeace’s forest campaign</a> has called for an end to
              deforestation, but our current climate emergency requires a genuine and just
              restoration of all-natural ecosystems, and a reduction in the degradation of the
              world&#39;s most critical landscapes.</p>

            <h2>Deforestation and climate change</h2>

            <p><b>Deforestation and climate change</b> are 2 things that are closely linked together
              since trees help a lot to clean the air with pollution and prevent them from going into
              the atmosphere.</p>

            <p>In recent years, between cities that pollute and various fires such as the Amazon and
              Australia fires, are greatly accelerating the process of changing the Earth&#39;s climate,
              increasing the temperature of the earth.</p>

            <p>According to <a
                href="https://www.nationalgeographic.com.es/ciencia/grandes-reportajes/el-cambio-climatico-es-una-realidad-2_9756/1"
                target="_blank"> national geographic</a> since 1992, humans have accelerated the process
              of climate change, but with the events that are taking place in 2020, together with the
              radical decrease in the use of cars in the main cities of the world for two or three
              months, they have shown how in a short time the atmosphere can recover</p>

            <p>So if periods of fifteen days were made without abusing the vehicles, the ozone layer
              would be repaired. This would reduce drought fires in places with low humidity and
              high heat.</p>

            <h2>Stop deforestation</h2>

            <p>To stop deforestation, everyone can collaborate, from reducing the use of plastic,
              picking up bottles when they go to the forest, or simply with our extension for the
              main browsers, with the <a href="https://opentabs.org/" target="_blank"> OpenTabs</a> extension, whenever
              you look for something, an
              ad will appear and you will be there. collaborating with indirect donations for
              deforestation.</p>

            <p>This is 100% free since we know that in the times we live, not everyone can afford to
              <a href="https://donorbox.org/opentabs" target="_blank"> donate money</a>, but it could help us if you can
              do it as well.</p>

            <p>In addition to this, you will be helping to fight poverty worldwide or diseases such as
              malaria, so if you want to help the cause download our extension and start
              contributing your grain of sand for free.</p>




